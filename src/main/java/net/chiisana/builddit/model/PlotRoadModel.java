package net.chiisana.builddit.model;

public class PlotRoadModel {
	private int roadXMin;
	private int roadXMax;
	private int roadZMin;
	private int roadZMax;

	public int getRoadXMin() {
		return roadXMin;
	}

	public void setRoadXMin(int roadXMin) {
		this.roadXMin = roadXMin;
	}

	public int getRoadXMax() {
		return roadXMax;
	}

	public void setRoadXMax(int roadXMax) {
		this.roadXMax = roadXMax;
	}

	public int getRoadZMin() {
		return roadZMin;
	}

	public void setRoadZMin(int roadZMin) {
		this.roadZMin = roadZMin;
	}

	public int getRoadZMax() {
		return roadZMax;
	}

	public void setRoadZMax(int roadZMax) {
		this.roadZMax = roadZMax;
	}
}
